def is_likert_scale(
    series: pd.Series, valid_range=(0, 10), missing_values={99, 98, 999, 998, 9999, 99999}, exclude_binary=True
):
    """
    Determine if a pandas Series is a Likert scale.
    Does not work if Likert scale is represented as string.

    Parameters:
        series (pd.Series): The pandas Series to check.
        valid_range (tuple, optional): The range (inclusive) of valid Likert scale values.
        missing_values (set, optional): The set of values indicating missing data.
        exclude_binary (boolean): ignore binary columns

    Returns:
        bool: True if the series is a Likert scale, False otherwise.
    """

    if series.empty:
        return False

    # Drop NaN values
    series = series.dropna()

    lower_bound, upper_bound = valid_range
    valid_values = set(range(lower_bound, upper_bound + 1))
    all_allowed_values = valid_values.union(missing_values)

    # Check if all values are within the allowed set
    if not series.isin(all_allowed_values).all():
        return False

    # Check if the series contains only binary values
    unique_values = set(series) - set(missing_values)

    if exclude_binary and len(unique_values) <= 2:
        return False

    if len(unique_values) <= 1:
        return False

    return True

def replace_missing_value_indicator(X: pd.DataFrame):
    """
    Uses heuristics to replace missing value indicators with pd.NA

    Always replaces -8, -9, -9999999998 -9999999999 and 9999999999 with pd.NA.
    Might remove correct values from certain numeric columns. Could be
    reduced to dtype=category, but it would exclude many missing indicators.
    """
    _X = X.copy()

    _X = _X.replace(
        [
            -9,
            -8,
            -9999999998,
            -9999999999,
            9999999999,
            "-9",
            "-8",
            "-9999999998",
            "-9999999999",
            "9999999999",
        ],
        pd.NA,
    )

    missing_indicators = [99, 98, 999, 998, 9999, 99999]

    # Replace missing values indicator in Likert scales only on columns defined as categorical in the Codebook
    # Get intersection so function works on reduced column set as well.
    for col in [col for col in COLS_CATEGORY if col in _X.columns]:
        if is_likert_scale(_X[col], valid_range=(0, 20), missing_values=missing_indicators, exclude_binary=False):
            _X[col] = _X[col].replace(missing_indicators, pd.NA)

    # Remove 0 for some columns because it indicates a missing value
    valid_cols = [col for col in COLS_ZERO_INDICATES_MISSING if col in _X.columns]
    _X[valid_cols] = _X[valid_cols].replace(0, pd.NA)

    return _X

