def __combine_hours_and_minutes_columns(df, column_pairs):
    """
    Converts pairs of hours and minutes columns into decimal representation of hours using vectorized operations.
    Treats missing values for hours or minutes as 0, but if both are missing, retains np.nan.

    Parameters:
    df (pd.DataFrame): The input DataFrame.
    column_pairs (list of tuples): List of tuples where each tuple contains
                                   (hours_column_name, minutes_column_name).

    Returns:
    pd.DataFrame: The DataFrame with additional columns for decimal hours.
    """
    df = df.copy()

    for hours_col, minutes_col in column_pairs:
        if hours_col in df.columns and minutes_col in df.columns:
            decimal_col = f"{hours_col}_{minutes_col}_decimal"

            # Convert hours and minutes to float, replacing NaN with 0
            hours = df[hours_col].astype("float").fillna(0)
            minutes = df[minutes_col].astype("float").fillna(0)

            # Compute decimal hours
            df[decimal_col] = hours + minutes / 60

            # Handle cases where both hours and minutes were originally NaN
            both_nan = df[hours_col].isna() & df[minutes_col].isna()
            df.loc[both_nan, decimal_col] = np.nan

            # Ensure the result is Float64
            df[decimal_col] = df[decimal_col].astype("Float64")

            # Drop original columns
            df = df.drop(columns=[hours_col, minutes_col])

    return df

def clean_df(df, background_df=None):
    """
    Preprocess the input dataframe to feed the model.
    # If no cleaning is done (e.g. if all the cleaning is done in a pipeline) leave only the "return df" command

    Parameters:
    df (pd.DataFrame): The input dataframe containing the raw data (e.g., from PreFer_train_data.csv or PreFer_fake_data.csv).
    background (pd.DataFrame): Optional input dataframe containing background data (e.g., from PreFer_train_background_data.csv or PreFer_fake_background_data.csv).

    Returns:
    pd.DataFrame: The cleaned dataframe with only the necessary columns and processed variables.
    """

    # Select columns
    X = df[[*COLS_TO_SELECT, "nomem_encr"]].copy()
    X = X.set_index("nomem_encr")

    # Optimize dtypes to get pandas Int dtypes.
    X = X.convert_dtypes(dtype_backend="pyarrow")

    X = replace_missing_value_indicator(X)

    valid_cat_cols = [col for col in COLS_CATEGORY if col in X.columns]
    X[valid_cat_cols] = X[valid_cat_cols].astype("category")

    X = __combine_hours_and_minutes_columns(X, COLS_HOURS_MINUTES_PAIRS)

    X = X.drop(columns=["outcome_available"], errors="ignore")

    X = feature_engine(X)

    return X


def predict_outcomes(df, background_df=None, model_path="flaml_model.joblib"):
    """
    Generate predictions using the saved model and the input dataframe.
    The predict_outcomes function accepts a Pandas DataFrame as an argument
    and returns a new DataFrame with two columns: nomem_encr and
    prediction. The nomem_encr column in the new DataFrame replicates the
    corresponding column from the input DataFrame. The prediction
    column contains predictions for each corresponding nomem_encr. Each
    prediction is represented as a binary value: '0' indicates that the
    individual did not have a child during 2021-2023, while '1' implies that
    they did.

    Parameters:
    df (pd.DataFrame): The input dataframe for which predictions are to be made.
    background_df (pd.DataFrame): The background dataframe for which predictions are to be made.
    model_path (str): The path to the saved model file (which is the output of training.py).

    Returns:
    pd.DataFrame: A dataframe containing the identifiers and their corresponding predictions.
    """

    ## This script contains a bare minimum working example

    # if "nomem_encr" not in df.columns:
    #    print("The identifier variable 'nomem_encr' should be in the dataset")

    # Load the model
    model = joblib.load(model_path)

    # Preprocess the fake / holdout data
    df = clean_df(df, background_df)

    # Generate predictions from model, should be 0 (no child) or 1 (had child)
    predictions = model.predict(df)

    # Output file should be DataFrame with two columns, nomem_encr and predictions
    df_predict = pd.DataFrame({"nomem_encr": df.index, "prediction": predictions})

    df_predict["prediction"] = df_predict["prediction"].astype("Int64")

    # Return only dataset with predictions and identifier
    return df_predict
